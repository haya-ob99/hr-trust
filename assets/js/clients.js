const updateContent = (swiper) => {
    const activeSlide = swiper.slides[swiper.activeIndex];
    if (!activeSlide) return;
    const contentId = activeSlide.getAttribute("data-content-id");
    const content = contentData[contentId];

    if (content) {
        document.querySelector(".client-content").innerHTML = `
        <div class="content">
          <h2>
            ${content.title} <strong>${content.companyName}</strong>
          </h2>
          <span>Contract duration: ${content.duration} Months , Year : ${content.year}</span>
          <div class="box goal">
            <h4>Section:</h4>
            <ul><li>${content.section}</li></ul>
          </div>
          <div class="box solution">
            <h4>Most prominent outputs:</h4>
            <ul>
              ${content.solution.map((item) => `<li>${item}</li>`).join("")}
            </ul>
          </div>
          <div class="box steps">
            <h4>Scope of Work:</h4>
            <ul>
              ${content.steps.map((step) => `<li>${step}</li>`).join("")}
            </ul>
          </div>
        </div>
        <div class="image">
          <img src="${content.image}" alt="Project Image" />
        </div>
      `;
    }
};

const swiper3 = new Swiper(".mySwiper3", {
    effect: "coverflow",
    grabCursor: true,
    centeredSlides: true,
    slidesPerView: "auto",
    loop: true,
    navigation: {
        nextEl: ".swiper-next",
        prevEl: ".swiper-prev",
    },
    coverflowEffect: {
        rotate: 0,
        stretch: 0,
        depth: 200,
        modifier: 1,
        slideShadows: false,
    },
    on: {
        slideChange: function () {
            updateContent(this);
        },
    },
});
updateContent(swiper3);